    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Boton para eliminar
        int confir = JOptionPane.showConfirmDialog(rootPane, "Desea eliminar el empleado con Id: \n" + codigoDelete.getText());
        
        System.out.println("Confir: " + confir);
        
        if( confir ==  JOptionPane.YES_OPTION){
            
           if(mdEmpleado.eliminarEmpleado(codigoDelete.getText())){
               JOptionPane.showMessageDialog(rootPane, "Empleado eliminado correctamente","Operacion Exitosa",JOptionPane.INFORMATION_MESSAGE); 
               codigoDelete.setText("");
               actVistaEmpleado();
            }else{
               JOptionPane.showMessageDialog(rootPane, "El empleado no existe","Operacion fallida",JOptionPane.ERROR_MESSAGE);
           }
            
        }else{
            if( confir == JOptionPane.NO_OPTION){
                JOptionPane.showMessageDialog(rootPane, "Empleado no eliminado");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        
        LinkedList<Empleado> empleadoLista = mdEmpleado.listaEmpleado();
        HashMap<Integer, ArrayList> listaNominas = new HashMap<Integer, ArrayList>();
        
        listaNominas = liquidacion.procesoMasivoNominaPrestaciones(empleadoLista);
        
        if(empleadoLista == null || listaNominas == null ){
            JOptionPane.showMessageDialog(rootPane, "No existen datos para procesar");
        }else{
            ListaNominasPrestaciones(listaNominas);
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed
private void listaEmpleado(){
        
        DefaultListModel model = new DefaultListModel();
        
        int index = 0;
        for (Empleado empleado : empleadoLista) {
            
            String data = (index+1) + ")  Id: " + empleado.getId() + " NOMBRE: " + empleado.getNombre() + " " + empleado.getApellido();
            
            model.add(index, data);
            index ++;
        }
        
        listEmpleados.setModel(model);
        //actVistaEmpleado();
        
    }
private void ListaNominasPrestaciones(HashMap<Integer, ArrayList> listaNominas){
     DefaultListModel model = new DefaultListModel();
        int index = 0;
        
        for (ArrayList i : listaNominas.values()){
            // Lista { id : (id - (nombre empleado + apellido) + nomina + prestacion) }
            String data = " Id:  " + i.get(0) + " NOMBRE:  " + i.get(1) + " NOMINA:  " + i.get(2) + " PRESTACION:  " + i.get(3);
            
            model.add(index, data);
            index ++;
            
        }
        
        liquidacionMasiva.setModel(model);
}
private void actVistaEmpleado(){  //Esto sirve para que se recargue automaticamente la pestaña - Un locaton. reload
        this.empleadoLista = mdEmpleado.listaEmpleado();
        this.listaEmpleado();
    }
