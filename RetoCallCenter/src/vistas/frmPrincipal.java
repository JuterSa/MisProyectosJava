    private void registerEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerEmpleadoActionPerformed
         try {
            if( cedulaEmpleadoUser.getText().equals("")  ||   nameEmpleado.getText().equals("") || lastNameEmpleado.getText().equals("") || (Integer.valueOf(salarioRegister.getText()) <= 0) ){
                JOptionPane.showMessageDialog(rootPane, "Los datos del nombre, apellido y salario son obligatorios","Debe de llenar todos los campos",JOptionPane.OK_OPTION);
            }else{
                
                empleado = mdEmpleado.validarCedula(cedulaEmpleadoUser.getText());
                
                if(empleado != null){
                    JOptionPane.showMessageDialog(rootPane, "El Empleado ya existe: \n" +
                                                            "Cedula: " + cedulaEmpleadoUser.getText() + " Datos Empleados: \n"
                                                          + "Id: \n" + empleado.getId() + " - "   + empleado.getNombre() + " " + empleado.getApellido(), "Operacion Denegada",JOptionPane.WARNING_MESSAGE); 
                }else{
                    if(mdEmpleado.crearEmpleado(nameEmpleado.getText(), lastNameEmpleado.getText(), Integer.valueOf(horasExtrasRegister.getText()), checkEmpleado.isSelected(), Integer.valueOf(salarioRegister.getText()), cedulaEmpleadoUser.getText())){

                        JOptionPane.showMessageDialog(rootPane, "Empleado guardado correctamente: \n" + nameEmpleado.getText() +" " + lastNameEmpleado.getText(),"Proceso Exitoso..",JOptionPane.INFORMATION_MESSAGE); 

                        nameEmpleado.setText("");
                        lastNameEmpleado.setText("");
                        cedulaEmpleadoUser.setText("");
                        salarioRegister.setText("0");
                        horasExtrasRegister.setText("0");
                        checkEmpleado.setSelected(false);
                        actVistaEmpleado();
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "Empleado NO guardado, intente de nuevo: \n" + nameEmpleado.getText() +" " + lastNameEmpleado.getText(),"Operacion Ivalida",JOptionPane.ERROR_MESSAGE); 
                    }
                }
            }
        } catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Error: \n Salario y Horas Extras son datos numericos"); 
        }
    }//GEN-LAST:event_registerEmpleadoActionPerformed

